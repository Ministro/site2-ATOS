let mapa;
let marker;
function initMapa() {
  if (mapa) { setTimeout(() => mapa.invalidateSize(), 100); return; }
  if (typeof L === "undefined") return;
  mapa = L.map("mapa").setView([-8.7622, -63.9019], 10);
  L.tileLayer("https://{s}.tile.openstreetmap.org/{z}/{x}/{y}.png", {
    attribution: "© OpenStreetMap contributors",
  }).addTo(mapa);
  mapa.on("click", (e) => setMarker(e.latlng.lat, e.latlng.lng));
  if (navigator.geolocation) {
    navigator.geolocation.getCurrentPosition(
      (pos) => setMarker(pos.coords.latitude, pos.coords.longitude),
      () => console.log("Não foi possível pegar a localização automática")
    );
  }
  setTimeout(() => mapa.invalidateSize(), 100);
}
function setMarker(lat, lng) {
  if (marker) mapa.removeLayer(marker);
  marker = L.marker([lat, lng]).addTo(mapa);
  mapa.setView([lat, lng], 15);
  document.getElementById("latitude").value = lat;
  document.getElementById("longitude").value = lng;
}

