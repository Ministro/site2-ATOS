function gerarTurboCards() {
  turboGrid.innerHTML = "";
  for (const key in turboPlanos) {
    const item = turboPlanos[key];
    const card = document.createElement("div");
    card.className = "turbo-card";
    card.dataset.name = key;
    const valorNum = item.valor ? parseFloat(String(item.valor).replace(",", ".")) : 0;
    card.dataset.price = isNaN(valorNum) ? "0" : String(valorNum);
    card.innerHTML = `
      <div class="turbo-logo">
        ${item.img ? `<img src="${item.img}" alt="${key} logo">` : `<span style="font-size:12px;color:#666">logo</span>`}
      </div>
      <div class="turbo-info">
        <div class="turbo-nome">${key}</div>
        <div class="turbo-valor">R$ ${item.valor}</div>
      </div>
    `;
    card.addEventListener("click", () => {
      const name = card.dataset.name;
      const isDisneyOrHbo = name === "Disney+" || name === "HBO Max";
      if ((planoNumeric === 800 || planoNumeric === 1000) && isDisneyOrHbo) return;
      card.classList.toggle("selected");
      atualizarTurboSelecionados();
      atualizarResumoMensalidade();
    });
    turboGrid.appendChild(card);
  }
}

function atualizarTurboSelecionados() {
  const selecionados = [];
  let total = 0;
  const selectedCards = Array.from(document.querySelectorAll(".turbo-card.selected"));
  let gratuitoUsado = false;
  for (const card of selectedCards) {
    const name = card.dataset.name;
    const price = parseFloat(card.dataset.price) || 0;
    const isIncluso = card.classList.contains("incluso");
    if ((planoNumeric === 500 || planoNumeric === 700) && isIncluso && !gratuitoUsado) {
      selecionados.push({ name, price: 0, free: true });
      gratuitoUsado = true;
    } else {
      selecionados.push({ name, price });
      total += price;
    }
  }
  turboTotalEl.textContent = `Total turbinar: R$ ${total.toFixed(2).replace(".", ",")}`;
  turboSelecionadosInput.value = selecionados.map((s) => s.name + (s.free ? " (GRÁTIS)" : "")).join(", ");
  turboTotalValueInput.value = total.toFixed(2);
}

function atualizarResumoMensalidade() {
  const resumoEl = document.getElementById("resumoMensalidade");
  const local = localSelect.value;
  const plano = planoSelect.value;
  if (!local || !plano || !dadosPorLocal[local]) {
    resumoEl.style.display = "none";
    mensalidadeTotal = 0;
    mensalidadeDescricao = "";
    return;
  }
  const valorPlanoStr = dadosPorLocal[local].planos[plano];
  if (!valorPlanoStr) { resumoEl.style.display = "none"; return; }
  let total = parseFloat(valorPlanoStr.replace(",", "."));
  const descricao = [plano];
  document.querySelectorAll(".turbo-card.selected").forEach((card) => {
    const nome = card.dataset.name;
    if (card.classList.contains("incluso")) { descricao.push(nome + " (GRÁTIS)"); return; }
    const valor = parseFloat(card.dataset.price) || 0;
    if (nome === "SEM STREAMING") return;
    if (valor > 0) { total += valor; descricao.push(nome); }
  });
  mensalidadeTotal = total;
  mensalidadeDescricao = descricao.join(" + ");
  resumoEl.style.display = "block";
  resumoEl.textContent = `MENSALIDADE: R$ ${total.toFixed(2).replace(".", ",")} — ${mensalidadeDescricao}`;
}

